    public void ganadores() {
        String resultado = "GANADORES PARA CADA DIGNIDAD\n\n";
        String ganadorGeneral = "";
        int votos = 0;
        int i = 0;
        ArrayList<Asociacion> asociaciones = Logueo.padron.getAsociaciones();
        boolean empate = false;
        for (Asociacion a : asociaciones) {
            if (!a.getNombre().equals("blanco")) {
                if (i == 0) {
                    ganadorGeneral = a.getNombre();
                    votos = a.votosTotalesGeneral();
                } else {
                    if (votos < a.votosTotalesGeneral()) {
                        ganadorGeneral = a.getNombre();
                        votos = a.votosTotalesGeneral();
                    } else if (votos == a.votosTotalesGeneral()) {
                        empate = true;
                    }
                }
                i++;
            }
        }
        Estudiante e = Logueo.padron.buscarEstudiante(Logueo.padron.buscarAsociacion(ganadorGeneral).getPresidenteGeneral().getEstudiante());
        resultado += "PRESIDENTE GENERAL: " + (empate ? "EMPATE" : (e.getNombre() + " " + e.getApellido() + "(" + ganadorGeneral + ")")) + "\n";
        e = Logueo.padron.buscarEstudiante(Logueo.padron.buscarAsociacion(ganadorGeneral).getVicepresidenteGeneral().getEstudiante());
        resultado += "VICEPRESIDENTE GENERAL: " + (empate ? "EMPATE" : (e.getNombre() + " " + e.getApellido() + "(" + ganadorGeneral + ")")) + "\n\n";

        for (Facultad f : Facultad.values()) {
            i = 0;
            votos = 0;
            empate = false;
            for (Asociacion as : asociaciones) {
                if (!as.getNombre().equals("blanco")) {
                    if (votos < as.getVotosFaculad().get(f)) {
                        ganadorGeneral = as.getNombre();
                        votos = votos = as.getVotosFaculad().get(f);
                    } else if (votos == as.getVotosFaculad().get(f)) {
                        empate = true;
                    }
                }
            }
            e = Logueo.padron.buscarEstudiante(Logueo.padron.buscarAsociacion(ganadorGeneral).getPresidentesFacultad().get(f).getEstudiante());
            resultado += "PRESIDENTE FACULTAD " + f.toString() + ": " + (empate ? "EMPATE" : (e.getNombre() + " " + e.getApellido() + "(" + ganadorGeneral + ")")) + "\n";
            e = Logueo.padron.buscarEstudiante(Logueo.padron.buscarAsociacion(ganadorGeneral).getVicepresidentesFacultad().get(f).getEstudiante());
            resultado += "VICEPRESIDENTE FACULTAD " + f.toString() + ":" + (empate ? "EMPATE" : (e.getNombre() + " " + e.getApellido() + "(" + ganadorGeneral + ")")) + "\n\n";
        }

        resultados.setText(resultado);
    }

