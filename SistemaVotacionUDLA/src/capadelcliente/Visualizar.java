    private void buscar(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buscar
        String resultado = "ESTADÍSTICAS ";
        if (categoria.getSelectedIndex() == 0) {
            resultado += "ASEUDLA GENERAL\n\n";
            for (Asociacion a : Logueo.padron.getAsociaciones()) {
                if (!a.getNombre().equals("blanco")) {
                    resultado += "PARTIDO " + a.getNombre() + "\n\n";
                    Estudiante e = Logueo.padron.buscarEstudiante(a.getPresidenteGeneral().getEstudiante());
                    resultado += "CANDIDATO PRESIDENTE: " + e.getNombre() + " " + e.getApellido() + "\n";

                    e = Logueo.padron.buscarEstudiante(a.getVicepresidenteGeneral().getEstudiante());
                    resultado += "CANDIDATO VICEPRESIDENTE: " + e.getNombre() + " " + e.getApellido() + "\n";
                    resultado += "VOTOS TOTALES: " + a.votosTotalesGeneral() + "\n";

                    for (Facultad f : Facultad.values()) {
                        resultado += "VOTOS DESDE " + f.toString() + ": " + a.getVotosGeneral().get(f) + "\n";
                    }
                    resultado += "\n";
                }
            }
            Asociacion a = Logueo.padron.buscarAsociacion("blanco");
            resultado += "VOTOS EN BLANCO\n\n"
                    + "ASEUDLA GENERAL\n\n";
            for (Facultad f : Facultad.values()) {
                resultado += "VOTOS DESDE " + f.toString() + ": " + a.getVotosGeneral().get(f) + "\n";
            }
        } else {
            String facultad = categoria.getSelectedItem().toString();
            resultado += "ASEUDLA FACULTAD " + facultad + "\n\n";
            for (Asociacion a : Logueo.padron.getAsociaciones()) {
                if (!a.getNombre().equals("blanco")) {
                    resultado += "PARTIDO " + a.getNombre() + "\n\n";
                    Estudiante e = Logueo.padron.buscarEstudiante(a.getPresidentesFacultad().get(Facultad.valueOf(facultad)).getEstudiante());
                    resultado += "CANDIDATO PRESIDENTE: " + e.getNombre() + " " + e.getApellido() + "\n";
                    e = Logueo.padron.buscarEstudiante(a.getVicepresidentesFacultad().get(Facultad.valueOf(facultad)).getEstudiante());
                    resultado += "CANDIDATO VICEPRESIDENTE: " + e.getNombre() + " " + e.getApellido() + "\n";
                    resultado += "VOTOS: " + a.getVotosFaculad().get(Facultad.valueOf(facultad)) + "\n\n";
                }
            }
            Asociacion a = Logueo.padron.buscarAsociacion("blanco");
            resultado += "VOTOS EN BLANCO: " + a.getVotosFaculad().get(Facultad.valueOf(facultad));
        }
        resultados.setText(resultado);
    }//GEN-LAST:event_buscar

