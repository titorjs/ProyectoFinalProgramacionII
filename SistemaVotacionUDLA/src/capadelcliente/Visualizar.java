    private void finalizarProceso(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_finalizarProceso
        if (Logueo.padron.getEstadoProceso() == 2) {
            if (Logueo.con.actualizarEstadoFinalizado()) {
                Logueo.padron.setEstadoFinalizado();
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo finalizar el proceso, inténtelo de nuevo más tarde");
            }
        } else {
            JOptionPane.showMessageDialog(null, "El proceso ya finalizó");
        }
    }//GEN-LAST:event_finalizarProceso

