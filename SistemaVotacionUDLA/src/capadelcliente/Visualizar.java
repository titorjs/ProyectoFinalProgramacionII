    private void estadisticas(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_estadisticas
        String resultado = "";
        for (Asociacion a : Logueo.padron.getAsociaciones()) {
            if (!a.getNombre().equals("blanco")) {
                resultado += "PARTIDO " + a.getNombre() + "\n\n"
                        + "PRESIDENTE GENERAL : " + Logueo.padron.buscarEstudiante(a.getPresidenteGeneral().getEstudiante()).getNombre() + " " + Logueo.padron.buscarEstudiante(a.getPresidenteGeneral().getEstudiante()).getApellido()
                        + "\nVICEPRESIDENTE GENERAL : " + Logueo.padron.buscarEstudiante(a.getVicepresidenteGeneral().getEstudiante()).getNombre() + " " + Logueo.padron.buscarEstudiante(a.getPresidenteGeneral().getEstudiante()).getApellido() + "\n\n";
                for (Facultad f : Facultad.values()) {
                    resultado += "VOTOS DESDE " + f.toString() + ": " + a.getVotosGeneral().get(f) + "\n";
                }
                resultado += "\n";
                for (Facultad f : Facultad.values()) {
                    resultado += "PRESIDENTE FACULTAD " + f.toString() + ": " + Logueo.padron.buscarEstudiante(a.getPresidentesFacultad().get(f).getEstudiante()).getNombre() + " " + Logueo.padron.buscarEstudiante(a.getPresidentesFacultad().get(f).getEstudiante()).getApellido() + "\n"
                            + "VICEPRESIDENTE FACULTAD " + f.toString() + ": " + Logueo.padron.buscarEstudiante(a.getVicepresidentesFacultad().get(f).getEstudiante()).getNombre() + " " + Logueo.padron.buscarEstudiante(a.getVicepresidentesFacultad().get(f).getEstudiante()).getApellido() + "\n"
                            + "VOTOS: " + a.getVotosFaculad().get(f) + "\n\n";
                }
            }
        }
        Asociacion a = Logueo.padron.buscarAsociacion("blanco");
        resultado += "VOTOS EN BLANCO\n\n"
                + "ASEUDLA GENERAL\n\n";
        for (Facultad f : Facultad.values()) {
            resultado += "VOTOS DESDE " + f.toString() + ": " + a.getVotosGeneral().get(f) + "\n";
        }
        resultado += "\n"
                + "ASEUDLA FACULTADES\n\n";
        for (Facultad f : Facultad.values()) {
            resultado += "VOTOS FACULTAD " + f.toString() + ": " + a.getVotosFaculad().get(f) + "\n";
        }
        resultados.setText(resultado);
    }//GEN-LAST:event_estadisticas

