    private void ingresarFirmas(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ingresarFirmas
        /**
         * Agrega el n√∫mero de firmas de manera local y en la base de datos, si
         * esta √∫ltima falla, no actualiza de manera local
         */
        if (asociacionesRegistradas.getSelectedIndex() >= 0) {
            String firmas = numeroFirmas.getText();
            if (ClaseValidacion.validarSoloNumeros(firmas)) {
                String asociacion = asociacionesRegistradas.getSelectedItem().toString();
                capadenegocio.Asociacion a = Logueo.padron.buscarAsociacion(asociacion);
                int numFirmas = Integer.parseInt(firmas);
                if (Logueo.con.establecerFirmasAsociacion(asociacion, numFirmas)) {
                    a.setFirmas(numFirmas);
                    JOptionPane.showMessageDialog(null, "Firmas actualizadas con √©xito a " + asociacion);
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo registrar las firmas\n"
                            + "int√©ntelo de nuevo m√°s tarde");
                }
            } else {
                JOptionPane.showMessageDialog(null, "El valor ingresado para el n√∫mero de firmas\n"
                        + "es incorrecto, verifique que sea entero y positivo");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Para proceder, seleccione una opci√≥n");
        }
    }//GEN-LAST:event_ingresarFirmas

