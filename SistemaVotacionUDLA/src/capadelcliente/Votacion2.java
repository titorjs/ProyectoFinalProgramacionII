    public void actualizar() {
        asociacionesRegistradas.removeAllItems();
        asociacionesRegistradas.addItem("Blanco");
        for (capadenegocio.Asociacion a : Logueo.padron.getAsociaciones()) {
            if (!a.getNombre().equals("blanco")) {
                asociacionesRegistradas.addItem(a.getNombre());
            }
        }

        String a[] = new String[3];
        String p[] = new String[3];
        String v[] = new String[3];
        int i = 0;

        for (Asociacion as : Logueo.padron.getAsociaciones()) {
            if (!as.getNombre().equals("blanco")) {
                Estudiante pr = Logueo.padron.buscarEstudiante(as.getPresidentesFacultad().get(Logueo.padron.buscarEstudiante(Logueo.idEstudiantVotando).getFacultad()).getEstudiante());;
                Estudiante vp = Logueo.padron.buscarEstudiante(as.getVicepresidentesFacultad().get(Logueo.padron.buscarEstudiante(Logueo.idEstudiantVotando).getFacultad()).getEstudiante());
                a[i] = as.getNombre();
                p[i] = pr.getNombre() + " " + pr.getApellido();
                v[i] = vp.getNombre() + " " + vp.getApellido();
                i++;
            }
        }

        as1.setText(a[0]);
        p1.setText(p[0]);
        v1.setText(v[0]);
        as2.setText(a[1]);
        p2.setText(p[1]);
        v2.setText(v[1]);
        as3.setText(a[2]);
        p3.setText(p[2]);
        v3.setText(v[2]);

    }

