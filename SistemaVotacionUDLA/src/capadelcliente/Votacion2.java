    private void votar(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_votar
        Calendar cal = Calendar.getInstance();
        String asociacionFacultad = (asociacionesRegistradas.getSelectedIndex() == 0)?"blanco":asociacionesRegistradas.getSelectedItem().toString();
        Estudiante e = Logueo.padron.buscarEstudiante(Logueo.idEstudiantVotando);
        String votoGeneral = (Logueo.votoTemporalGeneral.equals("Blanco")?"blanco":Logueo.votoTemporalGeneral);
        Facultad facultadVotante = e.getFacultad();
        boolean errorF = !(Logueo.con.agregarVoto(asociacionFacultad, facultadVotante.toString(), false));
        boolean errorG = !Logueo.con.agregarVoto(votoGeneral, facultadVotante.toString(), true);
        boolean errorR = !Logueo.con.añadirRegistro(facultadVotante.toString(), votoGeneral, asociacionFacultad, cal);
        boolean errorC = !Logueo.con.cambiarVotoEstudiante(Logueo.idEstudiantVotando);
        
        if (errorG) {
            System.out.println("Error durante el registro del voto General");
        }
        if(errorF){
            System.out.println("Error durante el registro del voto Facultad");
        }
        if(errorR){
            System.out.println("Error durante el registro de la papeleta");
        }
        if(errorC){
            System.out.println("Error durante el registro de la votación");
        }

        Logueo.padron.buscarAsociacion(votoGeneral).agregarVotoGeneral(facultadVotante);
        Logueo.padron.buscarAsociacion(asociacionFacultad).agregarVotoFacultad(facultadVotante);
        Voto voto = e.votar(facultadVotante.toString(), asociacionFacultad, cal.get(Calendar.HOUR_OF_DAY), cal.get(Calendar.MINUTE), cal.get(Calendar.SECOND));
        Logueo.padron.agregarVoto(voto);

        JOptionPane.showMessageDialog(null, "Voto registrado correctamente");
        Logueo l = new Logueo();
        l.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_votar

